        <h1 class="pagetitle">World Borders Dataset</h1>

	    <p>Provided by Bjorn Sandvik, <a href="/">thematicmapping.org</a></p>
	
	    <p>If you have any comments about this dataset, please post them on <a href="http://blog.thematicmapping.org/2008/03/world-borders-for-thematic-web-mapping.html">this page</a>.</p>
	
	    <p>Use this dataset with care, as several of the borders are disputed.</p>
	
	    <p>The original shapefile (world_borders.zip, 3.2 MB) was downloaded from the Mapping Hacks website:
	    <a href="http://www.mappinghacks.com/data/">http://www.mappinghacks.com/data/</a></p>
	
	    <p>The dataset is available under a Creative Commons <a href="http://creativecommons.org/licenses/by-sa/3.0/">Attribution-Share Alike License</a>. If you use this dataset, please provide a link to this website.</p>
	
	    <h2>Main changes</h2>
	    <ul>
	        <li> Polygons representing one country/area are merged into one feature.</li>
	        <li> Added ISO 3166-1 Country codes (alpha-2, alpha-3, numeric-3).</li>
	        <li> Various feature changes to make the dataset more compatible with ISO 3611-1. See "Readme.txt" for details.</li>
	        <li> Added <a href="http://unstats.un.org/unsd/methods/m49/m49regin.htm">region and sub-region codes</a> from UN Statistical Division.</li>
	        <li> Added longitude/latitude values for each country.</li>
	    </ul>
	
